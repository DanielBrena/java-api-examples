public class Aplicacion_1
{
	public static void main(String[] args) {
		
		/**
		Cuando se trabaja con caracteres, nosotros usamos los datos primitivos
		de tipo char
		*/

		//Dato primitivo
		char mi_char = 'a';

		System.out.println(mi_char);

		//Arreglo de char
		char[] array_de_char = {'a','b','c','d'};

		System.out.println(array_de_char);

		//Objeto de char
		Character objeto_char = new Character('a');
		System.out.println(objeto_char);

	}
}
